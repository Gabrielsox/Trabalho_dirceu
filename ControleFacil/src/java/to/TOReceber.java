package to;

import java.sql.Date;
import java.sql.ResultSet;

/**
 *
 * @author gabri
 */
public class TOReceber {
    private int id;
    private int id_cliente;
    private String descricao;
    private double valor;
    private Date dataVencimento;    
    private String observacao;
    
    public TOReceber(ResultSet rs) throws Exception {
        this.id = rs.getInt("id");
        this.id_cliente = rs.getInt("id_cliente");
        this.descricao = rs.getString("descricao");
        this.valor = rs.getDouble("valor");
        this.dataVencimento = rs.getDate("dataVencimento");        
        this.observacao = rs.getString("observacao");
        
    }
    
    public TOReceber() {
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the id_cliente
     */
    public int getId_cliente() {
        return id_cliente;
    }

    /**
     * @param id_cliente the id_cliente to set
     */
    public void setId_cliente(int id_cliente) {
        this.id_cliente = id_cliente;
    }

    /**
     * @return the descricao
     */
    public String getDescricao() {
        return descricao;
    }

    /**
     * @param descricao the descricao to set
     */
    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    /**
     * @return the valor
     */
    public double getValor() {
        return valor;
    }

    /**
     * @param valor the valor to set
     */
    public void setValor(double valor) {
        this.valor = valor;
    }

    /**
     * @return the dataVencimento
     */
    public Date getDataVencimento() {
        return dataVencimento;
    }

    /**
     * @param dataVencimento the dataVencimento to set
     */
    public void setDataVencimento(Date dataVencimento) {
        this.dataVencimento = dataVencimento;
    }

    /**
     * @return the observacao
     */
    public String getObservacao() {
        return observacao;
    }

    /**
     * @param observacao the observacao to set
     */
    public void setObservacao(String observacao) {
        this.observacao = observacao;
    }
}
